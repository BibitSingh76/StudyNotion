// Function to create a new course
exports.createCourse = async (req, res) => {
  try {
    // Get user ID from request object
    const userId = req.user.id

    // Get all required fields from request body
    let {
      courseName,
      courseDescription,
      whatYouWillLearn,
      price,
      tag: _tag,
      category,
      status,
      instructions: _instructions,
    } = req.body
    // Get thumbnail image from request files
    const thumbnail = req.files && req.files.thumbnailImage

    // Robustly parse tag and instructions. Accept either a JSON-stringified
    // array or a plain single id string (frontend sends single tag id).
    let tag
    try {
      if (typeof _tag === "string" && _tag.trim().startsWith("[")) {
        tag = JSON.parse(_tag)
      } else {
        tag = _tag
      }
    } catch (err) {
      tag = _tag
    }

    // Ensure tag is a single id (schema expects ObjectId, not array)
    if (Array.isArray(tag)) {
      tag = tag[0]
    }

    let instructions
    try {
      if (typeof _instructions === "string") {
        instructions = JSON.parse(_instructions)
      } else {
        instructions = _instructions
      }
    } catch (err) {
      instructions = _instructions
    }

    console.log("tag", tag)
    console.log("instructions", instructions)

    // Check if any of the required fields are missing
    if (
      !courseName ||
      !courseDescription ||
      !whatYouWillLearn ||
      !price ||
      !tag.length ||
      !thumbnail ||
      !category ||
      !instructions.length
    ) {
      return res.status(400).json({
        success: false,
        message: "All Fields are Mandatory",
      })
    }
    if (!status || status === undefined) {
      status = "Draft"
    }
    // Check if the user is an instructor
    const instructorDetails = await User.findById(userId, {
      accountType: "Instructor",
    })

    if (!instructorDetails) {
      return res.status(404).json({
        success: false,
        message: "Instructor Details Not Found",
      })
    }

    // Check if the tag given is valid
    const categoryDetails = await Category.findById(category)
    if (!categoryDetails) {
      return res.status(404).json({
        success: false,
        message: "Category Details Not Found",
      })
    }
    // Upload the Thumbnail to Cloudinary
    const thumbnailImage = await uploadImageToCloudinary(
      thumbnail,
      process.env.FOLDER_NAME
    )
    console.log(thumbnailImage)
    // Create a new course with the given details
    const newCourse = await Course.create({//save in mongoDB
      courseName,
      courseDescription,
      instructor: instructorDetails._id,
      whatYouWillLearn: whatYouWillLearn,
      price,
      tag,
      category: categoryDetails._id,
      thumbnail: thumbnailImage.secure_url,
      status: status,
      instructions,
    })

    // Add the new course to the User Schema of the Instructor
    await User.findByIdAndUpdate(
      {
        _id: instructorDetails._id,
      },
      {
        $push: {
          courses: newCourse._id,
        },
      },
      { new: true }
    )
    // Add the new course to the Categories
    const categoryDetails2 = await Category.findByIdAndUpdate(
      { _id: category },
      {
        $push: {
          courses: newCourse._id,
        },
      },
      { new: true }
    )
    console.log("HEREEEEEEEE", categoryDetails2)
    // Return the new course and a success message
    res.status(200).json({
      success: true,
      data: newCourse,
      message: "Course Created Successfully",
    })
  } catch (error) {
    // Handle any errors that occur during the creation of the course
    console.error(error)
    res.status(500).json({
      success: false,
      message: "Failed to create course",
      error: error.message,
    })
  }
}

// Edit Course Details
exports.editCourse = async (req, res) => {
  try {
    const { courseId } = req.body
    if (!courseId) {
      console.error('editCourse missing courseId in request body', req.body)
      return res.status(400).json({ success: false, message: 'courseId is required' })
    }
    const updates = req.body
    const course = await Course.findById(courseId)

    if (!course) {
      return res.status(404).json({ error: "Course not found" })
    }

    // Log incoming update payload to help debug server errors
    console.log("editCourse called with updates keys:", Object.keys(updates))
    console.log("editCourse updates body:", updates)
    if (req.files) {
      console.log("editCourse received files:", Object.keys(req.files))
    }

    // Fast-path: if this is a simple publish/unpublish request (only status plus courseId),
    // perform an atomic update to avoid touching other fields and reduce risk of errors.
    let updateKeys = updates && typeof updates === "object" && !Array.isArray(updates)
      ? Object.keys(updates)
      : []
    if (
      updateKeys.length === 2 &&
      updateKeys.includes("courseId") &&
      updateKeys.includes("status")
    ) {
      try {
        const updatedCourse = await Course.findByIdAndUpdate(
          courseId,
          { status: updates.status },
          { new: true }
        )
          .populate({
            path: "instructor",
            populate: {
              path: "additionalDetails",
            },
          })
          .populate("category")
          .populate("ratingAndReviews")
          .populate({
            path: "courseContent",
            populate: {
              path: "subSection",
            },
          })
          .exec()

        return res.json({
          success: true,
          message: "Course updated successfully",
          data: updatedCourse,
        })
      } catch (err) {
        console.error('editCourse status update failed:', err)
        return res.status(500).json({ success: false, message: err.message, error: err.stack })
      }
    }

    // If Thumbnail Image is found, update it
    if (req.files && req.files.thumbnailImage) {
      console.log("thumbnail update")
      const thumbnail = req.files.thumbnailImage
      const thumbnailImage = await uploadImageToCloudinary(
        thumbnail,
        process.env.FOLDER_NAME
      )
      course.thumbnail = thumbnailImage.secure_url
    }

    // Update only the fields that are present in the request body
    // `updateKeys` was computed earlier; reuse it here.

    for (const key of updateKeys) {
      // protect against accidental overwrite of course id
      if (key === "courseId") continue

      try {
        console.log(`Applying update key=${key} value=`, updates[key])
        if (key === "tag") {
          const val = updates[key]
          let parsed = val
          if (typeof val === "string" && val.trim().startsWith("[")) {
            parsed = JSON.parse(val)
          }
          // normalize to single id expected by schema
          course[key] = Array.isArray(parsed) ? parsed[0] : parsed
        } else if (key === "instructions") {
          const val = updates[key]
          course[key] = typeof val === "string" ? JSON.parse(val) : val
        } else {
          course[key] = updates[key]
        }
      } catch (err) {
        console.error(`Failed to apply update for key=${key}:`, err && err.message)
        // rethrow to allow outer catch to respond with 500 and logged error
        throw err
      }
    }

    await course.save()

    const updatedCourse = await Course.findOne({
      _id: courseId,
    })
      .populate({
        path: "instructor",
        populate: {
          path: "additionalDetails",
        },
      })
      .populate("category")
      .populate("ratingAndReviews")
      .populate({
        path: "courseContent",
        populate: {
          path: "subSection",
        },
      })
      .exec()

    res.json({
      success: true,
      message: "Course updated successfully",
      data: updatedCourse,
    })
  } catch (error) {
    // Log full error for debugging
    console.error('editCourse error:', error)
    res.status(500).json({
      success: false,
      message: error.message || "Internal server error",
      error: error.message,
      stack: error.stack,
    })
  }
}
