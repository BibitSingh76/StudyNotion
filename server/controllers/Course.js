// Delete the Course
exports.deleteCourse = async (req, res) => {
  try {
    const { courseId } = req.body

    // Find the course
    const course = await Course.findById(courseId)
    if (!course) {
      return res.status(404).json({ message: "Course not found" })
    }

    // Unenroll students from the course (guard against missing field)
    const studentsEnrolled = Array.isArray(course.studentsEnrolled)
      ? course.studentsEnrolled
      : []
    for (const studentId of studentsEnrolled) {
      try {
        await User.findByIdAndUpdate(studentId, {
          $pull: { courses: courseId },
        })
      } catch (err) {
        console.error(`Failed to unenroll student ${studentId}:`, err.message || err)
      }
    }

    // Delete sections and sub-sections
    const courseSections = Array.isArray(course.courseContent) ? course.courseContent : []
    for (const sectionId of courseSections) {
      // Delete sub-sections of the section
      const section = await Section.findById(sectionId)
      if (section) {
        const subSections = section.subSection
        for (const subSectionId of subSections) {
          await SubSection.findByIdAndDelete(subSectionId)
        }
      }

      // Delete the section
      await Section.findByIdAndDelete(sectionId)
    }

    // Delete the course
    await Course.findByIdAndDelete(courseId)

    return res.status(200).json({
      success: true,
      message: "Course deleted successfully",
    })
  } catch (error) {
    console.error(error)
    return res.status(500).json({
      success: false,
      message: "Server error",
      error: error.message,
    })
  }
}
